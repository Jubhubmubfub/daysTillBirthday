for (var i = daysUntilMyBirthday; i > 0; i--)
{
  if (i > 30)
  {
    console.log(i + " days left until my birthday.. so long.. :C");
  }
  else if (i < 5 && i > 1)
  {
    console.log(i + " DAYS LEFT UNTIL MY BIRTHDAY!");
  }
  else if (i === 1)
  {
    console.log(i + " DAY LEFT UNTIL MY BIRTHDAY!");
  }
  else if (i <= 30 && i > 5)
  {
    console.log("Only " + i + " days left until my birthday!");
  }
}
